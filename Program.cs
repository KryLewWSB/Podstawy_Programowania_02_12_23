int liczba1 = 2;

//3 Sposoby zwiększania zmiennych o + 1
liczba1 = liczba1 + 1; // 2 + 1 == 3 i 3 jest przypisywana do zmiennej
Console.WriteLine(liczba1);//3

liczba1 += 1;//To samo co wtżej tylko w skrocie
Console.WriteLine(liczba1);//4

liczba1++;//To samo co wcześniej za pomocą operatora inkrementacji
Console.WriteLine(liczba1);//5

liczba1 += 10;//zwiększenie o 10 wartości w zmiennej
Console.WriteLine(liczba1);//15